<template>
  <div class="customerDetails container">
    <router-link to="/" class="btn btn-default">返回</router-link>
    <h1 class="page-header">{{customer.name}}
      <span class="pull-right">
        <router-link to="" class="btn btn-primary" v-bind:to="'/edit/'+customer.id">编辑</router-link>
        <button class="btn btn-danger" v-on:click="deleteCustomer(customer.id)">删除</button>
      </span>
    </h1>
    <ul class="list-group">
      <li class="list-group-item">姓名：{{customer.age}}</li>
      <li class="list-group-item">年龄：{{customer.phone}}</li>
      <li class="list-group-item">邮箱：{{customer.email}}</li>
      <li class="list-group-item">公司：{{customer.companyId}}</li>
    </ul>
  </div>
</template>

<script>
  export default {
    name: 'customerDetails',
    data() {
      return {
        customer: ""
      }
    },
    methods: {
      fetchCustomers(id) {
        this.$http.get("http://localhost:3000/users/" + id).then(function(response) {
          this.customer = response.body;
        })
      },
      deleteCustomer(id){
        this.$http.delete("http://localhost:3000/users/" + id).then(function(response){
          this.$router.push({path:"/",query:{alert:"删除成功"}})
        })
      }
    },
    created() {
      this.fetchCustomers(this.$route.params.id);
    }
  }
</script>

